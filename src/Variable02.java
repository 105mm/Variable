public class Variable02 {
    public static void main(String[] args) {
        // 변수에 값 할당하기
        // 정수를 저장할 수 있는 변수 num1 선언

        int num1;

        // num1에 10을 저장
        // '='(대입연산자)

        num1 = 10;

        // num1 = "java"; <- 오류

        int num2;
        num2 = 20;

        System.out.println(num2);
        System.out.println(num1);
    }
}
